package Assignmentlooping;

public class naturalnumsum {

	public static void main(String[] args) {
		//Program to find the sum of natural numbers from 1 to 1000.
		int n1 = 0;
		int n2 = 1000;
		System.out.println("************Display******************");
		for(int i = 1; i <= n2; ++i)
			n1 = n1 + i;

			System.out.println("Sum is :"+n1);
	}

}
